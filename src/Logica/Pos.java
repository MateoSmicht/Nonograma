package Logica;

public class Pos {
    private final int fila;
    private final int columna;

    public Pos(int fila, int columna) {
        this.fila = fila;
        this.columna = columna;
    }

    public int fila() {
        return fila;
    }

    public int columna() {
        return columna;
    }


}

